        function transliterate(text) {
            const ru = {
                'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 
                'е': 'e', 'ё': 'e', 'ж': 'zh', 'з': 'z', 'и': 'i', 
                'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm', 'н': 'n',
                'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't',
                'у': 'u', 'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch',
                'ш': 'sh', 'щ': 'sch', 'ъ': '', 'ы': 'y', 'ь': '',
                'э': 'e', 'ю': 'yu', 'я': 'ya'
            };

            return text.toLowerCase().split('').map(char => {
                return ru[char] || char;
            }).join('').replace(/[^a-z0-9_]/g, '_').replace(/_+/g, '_');
        }

        // Функция для автоматической генерации кода поля из имени
        function updateFieldName() {
            const labelInput = document.getElementById('fieldLabel');
            const nameInput = document.getElementById('fieldName');
            
            // Генерируем код поля только если он пустой или был автоматически сгенерирован
            if (nameInput.value === '' || nameInput.dataset.autoGenerated === 'true') {
                nameInput.value = transliterate(labelInput.value);
                nameInput.dataset.autoGenerated = 'true';
            }
        }

        document.getElementById('fieldLabel').addEventListener('input', function() {
            updateFieldName();
        });

