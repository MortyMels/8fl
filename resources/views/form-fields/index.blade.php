        async function checkUniqueness(input, field) {
            const formId = '{{ $form->id }}';
            const value = input.value;
            
            try {
                const response = await fetch(`/api/forms/${formId}/fields/check-unique?field=${field}&value=${encodeURIComponent(value)}`);
                const data = await response.json();
                
                const errorDiv = input.nextElementSibling;
                if (!data.unique) {
                    input.classList.add('border-red-500');
                    if (!errorDiv || !errorDiv.classList.contains('text-red-500')) {
                        const error = document.createElement('div');
                        error.className = 'text-red-500 text-sm mt-1';
                        error.textContent = `Это ${field === 'label' ? 'название' : 'имя'} уже используется`;
                        input.parentNode.insertBefore(error, input.nextSibling);
                    }
                } else {
                    input.classList.remove('border-red-500');
                    if (errorDiv && errorDiv.classList.contains('text-red-500')) {
                        errorDiv.remove();
                    }
                }
            } catch (error) {
                console.error('Ошибка при проверке уникальности:', error);
            }
        }

        function updateLabel() {
